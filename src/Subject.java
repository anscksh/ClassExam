
public class Subject {

	private String subjectName;
	private int subjectScore;
	
	public void setSubjectName(String name) {
		subjectName = name;
	}
	public String getSubjectName() {
		return subjectName;
	}
	
	public void setSubjectScore(int score) {
		subjectScore = score;
	}
	public int getSubjectScore() {
		return subjectScore;
	}

}